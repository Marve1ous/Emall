package com.emall.spring.entity;

public class Repertory {
    private String repertoryid;

    private String repertoryname;

    private String repertoryaddr;

    private String repertoryinfo;

    private String repertorytel;

    public String getRepertoryid() {
        return repertoryid;
    }

    public void setRepertoryid(String repertoryid) {
        this.repertoryid = repertoryid;
    }

    public String getRepertoryname() {
        return repertoryname;
    }

    public void setRepertoryname(String repertoryname) {
        this.repertoryname = repertoryname;
    }

    public String getRepertoryaddr() {
        return repertoryaddr;
    }

    public void setRepertoryaddr(String repertoryaddr) {
        this.repertoryaddr = repertoryaddr;
    }

    public String getRepertoryinfo() {
        return repertoryinfo;
    }

    public void setRepertoryinfo(String repertoryinfo) {
        this.repertoryinfo = repertoryinfo;
    }

    public String getRepertorytel() {
        return repertorytel;
    }

    public void setRepertorytel(String repertorytel) {
        this.repertorytel = repertorytel;
    }
}