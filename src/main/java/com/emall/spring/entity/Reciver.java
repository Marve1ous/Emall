package com.emall.spring.entity;

public class Reciver {
    private String reciverid;

    private String customerid;

    private String recivername;

    private String reciveraddr;

    private String recivertel;

    public String getReciverid() {
        return reciverid;
    }

    public void setReciverid(String reciverid) {
        this.reciverid = reciverid;
    }

    public String getCustomerid() {
        return customerid;
    }

    public void setCustomerid(String customerid) {
        this.customerid = customerid;
    }

    public String getRecivername() {
        return recivername;
    }

    public void setRecivername(String recivername) {
        this.recivername = recivername;
    }

    public String getReciveraddr() {
        return reciveraddr;
    }

    public void setReciveraddr(String reciveraddr) {
        this.reciveraddr = reciveraddr;
    }

    public String getRecivertel() {
        return recivertel;
    }

    public void setRecivertel(String recivertel) {
        this.recivertel = recivertel;
    }
}